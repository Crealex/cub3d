#include <cub3d.h>

int	is_two_spaces(int i, int j, char **file)
{
	if (file[i][j] && file[i][j + 1])
	{
		if (file[i][j] == ' ' && file[i][j + 1] == ' ')
			return (1);
	}
	return (0);
}
